package com.bartosztobiasz;

public class Main {
    public static void main(String[] args) {
        // using augmented/compound assignment operator
        int x = 1;

        x += 2; // x = x + 2;
        System.out.println(x);

        x -= 2; // x = x - 2;
        System.out.println(x);

        x *= 2; // x = x * 2;
        System.out.println(x);

        x /= 2; // x = x / 2;
        System.out.println(x);
    }
}
